package SubPackage;

public class Overloading {

	public void add(int x , int y)
	{
		int z= x+y;
		System.out.println(z);
		
	}
	
	public void add(int x , int y, int z)
	{
		int w= x+y+z;
		System.out.println(w);
		
	}
	
	public void add(float x , float y)
	{
		float z= x+y;
		System.out.println(z);
		
	}
	
}
